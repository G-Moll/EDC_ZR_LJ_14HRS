#include <iostream>
using namespace std;

int main() {
    // ARITMETIC ASSIGNMENT
    // =    assignment
    // +=   plus
    // /=   divide
    // *=   multiply
    // -=   substraction
    // %=   modulus

    int x = 20;
    cout << "x = " << x << endl;
    x += 10;
    cout << "x = " << x << endl;
    x /= 15;
    cout << "x = " << x << endl;
    x *= 5;
    cout << "x = " << x << endl;
    x -= 5;
    cout << "x = " << x << endl;
    x %= 2;
    cout << "x = " << x << endl;
    x = 2;
    cout << "x = " << x << endl;
    x = (2 + 5 * 3);
    cout << "x = " << x << endl;

    return 0;
}
