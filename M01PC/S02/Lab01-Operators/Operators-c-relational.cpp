#include <iostream>
using namespace std;

int main() {
    // RELATIONAL OPERATORS
    cout << "1 < 1: " << ( 1 < 1 ) << endl; 
    cout << "1 <= 1: " << ( 1 <= 1 ) << endl;
    cout << "1 > 1: " << ( 1 > 1 ) << endl;
    cout << "1 >= 1: " << ( 1 >= 1 ) << endl;
    cout << "1 == 1: " << ( 1 == 1 ) << endl;
    cout << "1 != 1: " << ( 1 != 1 ) << endl;

    return 0;
}
